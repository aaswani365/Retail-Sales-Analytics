/*------------------------------------------------------------------------------
KPI 016 : Orders by Employee
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

How many customer orders has each employee processed?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

This KPI helps evaluate employee productivity by measuring the number
of customer orders handled by each employee.

It assists management in identifying top-performing employees,
balancing workloads, and recognizing training opportunities.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Displays the total number of orders processed by each employee,
ranked from highest to lowest.

------------------------------------------------------------------------------
*/

SELECT

    E.EmployeeID                     AS [Employee ID],
    CONCAT(E.FirstName, ' ', E.LastName)                   AS [Employee Name],
    COUNT(O.OrderID)                 AS [Total Orders Processed]

FROM dbo.[Order] O

INNER JOIN dbo.Employee E
    ON O.EmployeeID = E.EmployeeID

GROUP BY

    E.EmployeeID,
    CONCAT(E.FirstName, ' ', E.LastName) 

ORDER BY

    [Total Orders Processed] DESC,
    [Employee Name];

PRINT 'KPI 016 : Orders by Employee Generated Successfully.';
PRINT '--------------------------------------------------------------';
GO

/*------------------------------------------------------------------------------
KPI 017 : Top 10 Highest Value Orders
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

Which customer orders generated the highest revenue?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

High-value orders contribute significantly to overall revenue. Identifying
these orders helps management recognize premium customers, evaluate
large purchases, and develop targeted marketing strategies.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Displays the Top 10 highest-value customer orders based on Net Amount.

