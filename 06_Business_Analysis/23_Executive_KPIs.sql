*/

SELECT TOP (10)

    O.OrderNumber                     AS [Order Number],
    CONCAT(C.FirstName, ' ', C.LastName)                     AS [Customer Name],
    S.StoreName                       AS [Store Name],
    CONCAT(E.FirstName, ' ', E.LastName)                     AS [Processed By],
    CAST(O.OrderDate AS DATE)         AS [Order Date],
    O.NetAmount                       AS [Order Value]

FROM dbo.[Order] O

INNER JOIN dbo.Customer C
    ON O.CustomerID = C.CustomerID

INNER JOIN dbo.Store S
    ON O.StoreID = S.StoreID

INNER JOIN dbo.Employee E
    ON O.EmployeeID = E.EmployeeID

ORDER BY

    O.NetAmount DESC,
    O.OrderDate DESC;

PRINT 'KPI 017 : Top 10 Highest Value Orders Generated Successfully.';
PRINT '--------------------------------------------------------------';
GO

/*------------------------------------------------------------------------------
KPI 018 : Order Status Distribution
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

What is the distribution of customer orders across different order
statuses?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Monitoring order status distribution helps management evaluate the
overall efficiency of the order fulfillment process.

A high percentage of completed orders indicates smooth operations,
while a high number of pending or cancelled orders may highlight
operational bottlenecks requiring attention.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Displays the number and percentage of orders in each status, providing
an overview of the business's operational performance.

------------------------------------------------------------------------------
*/

SELECT

    OS.StatusName                           AS [Order Status],

    COUNT(O.OrderID)                             AS [Total Orders],

    CAST
    (
        COUNT(O.OrderID) * 100.0 /
        SUM(COUNT(O.OrderID)) OVER ()
        AS DECIMAL(10,2)
    )                                            AS [Order Percentage (%)]

FROM dbo.[Order] O

INNER JOIN dbo.OrderStatus OS
    ON O.OrderStatusID = OS.OrderStatusID

GROUP BY

    OS.StatusName

ORDER BY

    [Total Orders] DESC;

PRINT 'KPI 018 : Order Status Distribution Generated Successfully.';
PRINT '==============================================================';
