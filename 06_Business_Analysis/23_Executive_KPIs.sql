/*------------------------------------------------------------------------------
KPI 011 : Average Items Per Order
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

On average, how many products are purchased in each customer order?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Average Items Per Order measures customer purchasing behavior and helps
evaluate cross-selling, up-selling, and basket size.

A higher average indicates that customers are buying more products in
each transaction.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Shows the average number of items purchased per order across the business.

------------------------------------------------------------------------------
*/

SELECT

    CAST(AVG(OrderItemCount) AS DECIMAL(10,2)) AS [Average Items Per Order]

FROM
(
    SELECT

        OrderID,
        SUM(Quantity) AS OrderItemCount

    FROM dbo.OrderItem

    GROUP BY

        OrderID

) AS OrderSummary;

PRINT 'KPI 011 : Average Items Per Order Generated Successfully.';
PRINT '--------------------------------------------------------------';
GO

/*------------------------------------------------------------------------------
KPI 012 : Average Daily Orders
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

On average, how many customer orders are placed each day?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Average Daily Orders measures the business's daily operational volume
and helps management understand customer demand patterns.

This KPI is useful for workforce planning, inventory replenishment,
and sales forecasting.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Shows the average number of customer orders placed per business day.

------------------------------------------------------------------------------
*/

SELECT

    CAST
    (
        COUNT(OrderID) * 1.0 /
        COUNT(DISTINCT CAST(OrderDate AS DATE))
        AS DECIMAL(10,2)
    ) AS [Average Daily Orders]

FROM dbo.[Order];

PRINT 'KPI 012 : Average Daily Orders Generated Successfully.';
PRINT '--------------------------------------------------------------';
GO

/*------------------------------------------------------------------------------
KPI 013 : Highest Items in a Single Order
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

Which customer order contains the highest number of items purchased?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Identifying the largest order helps the business understand peak
customer purchasing behavior and evaluate opportunities for bulk sales,
promotional campaigns, and inventory planning.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Displays the order with the highest total quantity of items purchased,
along with the customer, order date, and total order value.

------------------------------------------------------------------------------
*/

SELECT TOP (1)

    O.OrderNumber                          AS [Order Number],
    C.FirstName							   AS [First Name],
	C.LastName							   As [Last Name],
    CAST(O.OrderDate AS DATE)              AS [Order Date],
    SUM(OI.Quantity)                       AS [Total Items Purchased],
    O.NetAmount                            AS [Order Value]

FROM dbo.[Order] O

INNER JOIN dbo.Customer C
    ON O.CustomerID = C.CustomerID

INNER JOIN dbo.OrderItem OI
    ON O.OrderID = OI.OrderID

GROUP BY

    O.OrderNumber,
    C.FirstName,
	C.LastName,
    O.OrderDate,
    O.NetAmount

ORDER BY

    [Total Items Purchased] DESC,
    [Order Value] DESC;

PRINT 'KPI 013 : Highest Items in a Single Order Generated Successfully.';
PRINT '--------------------------------------------------------------';
GO

