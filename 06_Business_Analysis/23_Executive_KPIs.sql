/*------------------------------------------------------------------------------
KPI 010 : Orders by Status
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

How many orders belong to each order status?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Tracking orders by status helps management monitor order fulfillment,
pending orders, completed orders, cancelled orders and operational
efficiency.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Provides a complete breakdown of all orders by their current status.

------------------------------------------------------------------------------
*/

SELECT

    OS.StatusName AS [Order Status],
    COUNT(O.OrderID)   AS [Total Orders]

FROM dbo.[Order] O

INNER JOIN dbo.OrderStatus OS
    ON O.OrderStatusID = OS.OrderStatusID

GROUP BY

    OS.StatusName

ORDER BY

    [Total Orders] DESC;

PRINT 'KPI 010 : Orders by Status Generated Successfully.';
