KPI 011 : Average Items Per Order
------------------------------------------------------------------------------*/

/*
------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

On average, how many products are purchased in each customer order?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Average Items Per Order measures customer purchasing behavior and helps
evaluate cross-selling, up-selling, and basket size.

A higher average indicates that customers are buying more products in
each transaction.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

Shows the average number of items purchased per order across the business.

------------------------------------------------------------------------------
