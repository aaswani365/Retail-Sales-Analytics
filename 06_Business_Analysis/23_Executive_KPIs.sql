------------------------------------------------------------------------------
*/

SELECT

    OS.StatusName                           AS [Order Status],

    COUNT(O.OrderID)                             AS [Total Orders],

    CAST
    (
        COUNT(O.OrderID) * 100.0 /
        SUM(COUNT(O.OrderID)) OVER ()
        AS DECIMAL(10,2)
    )                                            AS [Order Percentage (%)]

FROM dbo.[Order] O

INNER JOIN dbo.OrderStatus OS
    ON O.OrderStatusID = OS.OrderStatusID

GROUP BY

    OS.StatusName

ORDER BY

    [Total Orders] DESC;

