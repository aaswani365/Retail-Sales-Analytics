KPI 001 : Business Overview
------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------
Business Question
------------------------------------------------------------------------------

What is the current size and operational footprint of the business?

------------------------------------------------------------------------------
Business Importance
------------------------------------------------------------------------------

Provides an executive summary of the business by reporting the total
number of customers, stores, products, suppliers, employees, orders,
payments, returns and inventory records.

This KPI serves as the foundation for understanding overall business
scale before reviewing detailed performance metrics.

------------------------------------------------------------------------------
Expected Insight
------------------------------------------------------------------------------

A single-row summary showing the overall size of the business across
all major entities.

------------------------------------------------------------------------------*/

SELECT

    DB_NAME() AS DatabaseName,

    (
        SELECT COUNT(*)
        FROM dbo.Customer
    ) AS TotalCustomers,

    (
        SELECT COUNT(*)
        FROM dbo.Store
    ) AS TotalStores,

    (
        SELECT COUNT(*)
        FROM dbo.Product
    ) AS TotalProducts,

    (
        SELECT COUNT(*)
        FROM dbo.Supplier
    ) AS TotalSuppliers,

    (
        SELECT COUNT(*)
        FROM dbo.Employee
    ) AS TotalEmployees,

    (
        SELECT COUNT(*)
        FROM dbo.[Order]
    ) AS TotalOrders,

    (
        SELECT COUNT(*)
        FROM dbo.OrderItem
    ) AS TotalOrderItems,

    (
        SELECT COUNT(*)
        FROM dbo.Payment
    ) AS TotalPayments,

    (
        SELECT COUNT(*)
        FROM dbo.[Return]
    ) AS TotalReturns,

    (
        SELECT COUNT(*)
        FROM dbo.Inventory
    ) AS InventoryRecords;

PRINT 'KPI 001 : Business Overview Generated Successfully.';
